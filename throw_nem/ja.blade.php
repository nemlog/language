@section('lang04')
nemは他の仮想通貨同様、仮想通貨の取引所で取引(売買)されています。nemを取り扱っている日本の取引所zaifでは
日本円やビットコインに交換することができます。国内ではnemを取り扱っている取引所は多くないのですが、
海外の取引所ではnemを扱っていることが多いです。以下に国内取引所zaifを含む、取引所のリンクを記載しますので、
