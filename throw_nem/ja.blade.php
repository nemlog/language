@section('lang02')
投げ銭できるものが数えるほどしかなかったように思います。そこでnemを使ってnemlogを作ってみようと思い立ちました。
インターネットで無料の情報があふれ、なんでも安く買い叩こうとする昨今、
ブログ記事なんかの情報への対価を払うことを躊躇してしまうのも事実。
100円のジュースは買うのに、100円のアプリは買わない。そんな人結構いるんじゃないです？<br/><br/>
日本はチップという文化がないため 寄付する行為に対して奥手な印象ですが、
nemlogが目指すものは「よいもの」に対して、気持ちをキチンと伝えられる文化。
良い情報を提供してくれるnemberにはガッツリ投げnemしようじゃありませんか！！
強制じゃないんです、あくまで”気持ち”が大事なのかなと考えています。<br/><br/>
投げnemで仮想通貨nemの価値をちょっとでも向上できれば！そんな思いを込めたnemlogです。
@endsection

