@section('lang01')
nemlogウォレットにnemを送金する前に本ページの設定を確実に実施し、<br />
nemlogのウォレットアドレスと外部ウォレットのアドレスが同じか必ず確認して下さい。<br />
未実施の場合は受け取ったnemを送金することができなくなる可能性があります。
@endsection

@section('lang02')
nano walletをダウンロードします。<br />
nano walletはnemの公式ウォレットでnemlogウォレットと連携可能です。
nem換金の際は、nano walletから取引所に送金することになりますので、インストールしておきましょう。<br />
アドレスはこちら
@endsection

@section('lang03')
インストールが完了したら、起動してアカウント作成プライベートキーウォレットを選択。
@endsection

@section('lang04')
Mainnetを選択し、ウォレットのパスワードを設定します。ここで設定したパスワードも忘れないように！
@endsection

@section('lang05')
次にウォレットの名前を入力します。名前はなんでもよいです。
@endsection

@section('lang06')
次にnemlogで出力されたプライベートキーを入力します。
@endsection

@section('lang07')
ウォレットアドレスが表示されますので、<span class="red-text fw7">
このアドレスがnemlogでメモしたアドレスおよびプロフィールに表示されているアドレスと一致しているか必ず確認して下さい！</span>
@endsection

@section('lang08')
一致しない場合は入手したnemが取りだせなくなりますので、必ずnemlog運営に連絡下さい！
@endsection

@section('lang09')
念のためウォレットファイルもダウンロードしておきます。あとは順に進んでいけばOK！<br />
ログインで作成したウォレットにアクセスするとnemlogアドレスと同期完了です！
@endsection

@section('lang10')
nem walletをダウンロード
@endsection

@section('lang11')
アプリを起動し、アカウントを追加をタップ(その前にパスワード設定があるかも・・・。)
@endsection

@section('lang12')
鍵の入力をタップ
@endsection

@section('lang13')
nemlogで出力された秘密鍵とウォレット名(何でもよいです)を入力
@endsection

@section('lang14')
設定を完了したらQRコードをタップすると、ウォレットアドレスが表示されますので、<span class="red-text fw7">
このアドレスがnemlogでメモしたアドレスおよびプロフィールに表示されているアドレスと一致しているか必ず確認して下さい！</span><br />
一致しない場合は入手したnemが取りだせなくなりますので、必ずnemlog運営に連絡下さい！
@endsection



