nemlogとは暗号通貨nemを使用した寄付機能付きブログコミュニケーションサービスです。
ブログを投稿したNEMber(nemホルダー、nem愛好家)に対する共感・応援機能として、
nemを寄付(投げ銭)する機能をつけました。ブログを通じてNEMber同士がお互いにnemを寄付しあえるような環境づくりを目指して本サービスをスタートしました。
@endsection

