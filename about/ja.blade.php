@section('lang04')
nemlogはNEMber皆様のご厚意で成り立つサービスですので、活動内容を正しく開示し、サービスの透明性を保持するように努めます。
本サービスはnemブロックチェーンをそのまま利用している為、全てのnem入出金履歴はそのブロックチェーン上に記録されており、
誰もがその履歴を参照できるようになっています。ユーザー同士の投げnemの履歴やnemlogへの寄付履歴も もちろん開示されています。
また、みなさまから贈っていだいたnemlogに対する寄付金はnemlogの追加開発の費用として大切に利用させていただきます。
nemlogの活動内容・更新情報は本サイト内で積極的に開示し、透明性のある運営を目指します。
